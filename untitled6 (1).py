from tensorflow.keras.applications import MobileNetV2
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, Dropout
from tensorflow.keras.optimizers import Adam

# --- Load base MobileNetV2 ---
base_model = MobileNetV2(
    weights='imagenet',
    include_top=False,
    input_shape=(128, 128, 3),
    pooling='avg'   # ü©µ This fixes multiple output issue
)

# --- Build final model ---
model = Sequential([
    base_model,
    Dropout(0.3),
    Dense(128, activation='relu'),
    Dense(15, activation='softmax')  # adjust class count
])

# Compile
model.compile(optimizer=Adam(learning_rate=0.0001),
              loss='categorical_crossentropy',
              metrics=['accuracy'])


# üèãÔ∏è Train model (faster than VGG16)
history = model.fit(
    datagen.flow_from_directory(data_path, target_size=(128,128), batch_size=32, subset='training'),
    validation_data=datagen.flow_from_directory(data_path, target_size=(128,128), batch_size=32, subset='validation'),
    epochs=10
)

# üíæ Save the trained model
model.save("/content/animal_classifier_mobilenetv2.keras")
print("‚úÖ MobileNetV2 model saved successfully as animal_classifier_mobilenetv2.keras")

